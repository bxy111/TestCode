public class Dog{
	private String type;
	private double price;
	private String color;
	public String getType(){
		return type;
	}
	public void setType(String  str){
		type=str;
	}
	public String getColor(){
		return color;
	}
	public void setType(String  str){
		color=str;
	}
	public double getPrice(){
		return price;	
	}
	public void setPrice(double num){
		price=num;
	}
}